def square(x):
	"""
	Takes a number x and squares it.

	Parameters:
	-----------
	x, float or int: 
		Number which is to be squared.

	Returns:
    --------
    float:
    	Square of x

    Examples:
    ---------

    >>> square(2)
    4

    >>> square(8)
    64

    >>> square(-1)
    1
	"""
	return x*x